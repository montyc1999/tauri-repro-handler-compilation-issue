// cargo build --bin fast

/// Replacement for `tauri::generate_handler!` that wraps each handler in a function,
/// instead of putting them all in one big closure. Speeds up incremental compilation time.
#[macro_export]
macro_rules! generate_split_handler {
    // Helper for extracting the tauri command name
    // e.g. commands::studio::foo -> "foo"
    (@last $last:ident) => { stringify!($last) };
    (@last $head:ident :: $($tail:tt)+) => { generate_split_handler!(@last $($tail)+) };

    ( $( $(#[$meta:meta])* $($seg:ident)::+ ),* $(,)? ) => {
        |invoke: tauri::ipc::Invoke<tauri::Wry>| -> bool {
            let cmd = invoke.message.command();
            match cmd {
                $(
                    $(#[$meta])*
                    generate_split_handler!(@last $($seg)::+) => {
                        fn handler_wrapper() -> impl Fn(tauri::ipc::Invoke<tauri::Wry>) -> bool {
                            tauri::generate_handler![$($seg)::+]
                        }
                        handler_wrapper()(invoke)
                    },
                )*
                _ => false,
            }
        }
    };
}

